export const getLandmarkById = (id: string): BaseLandmark | undefined =>
  LANDMARKS.find((l) => l.id === id)

export const getLandmarksByCountry = (countryId: string): BaseLandmark[] =>
  LANDMARKS.filter((l) => l.countryId === countryId)

export const getLandmarksByCity = (cityId: string): BaseLandmark[] =>
  LANDMARKS.filter((l) => l.cityId === cityId)

export const getLandmarksByLevel = (level: BaseLandmark['level']): BaseLandmark[] =>
  LANDMARKS.filter((l) => l.level === level)

export const getLandmarksVisibleAtAltitude = (altitudeKm: number): BaseLandmark[] =>
  LANDMARKS.filter((l) => altitudeKm <= l.appearZoomLevel)
